package com.huidong.qzh.standard.service;

import java.util.List;

public interface ProductAttributeService {

    /**
     * 通过产品ID获取产品属性
     * @param goodsId
     * @return
     */
    List listProductAttribute(Integer goodsId);

    /**
     * PC端 发布产品 1
     * 通过分类ID 获取品牌的属性
     * @param categoryId
     * @return
     */
    List listBrandAttribute(Integer categoryId);

    /**
     * 获取这个分类下的所有属性 及 属性选项
     * @param categoryId
     * @return
     */
    List listAttributeOption(Integer categoryId);
}
