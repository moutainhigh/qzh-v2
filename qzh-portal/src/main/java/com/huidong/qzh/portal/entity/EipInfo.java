package com.huidong.qzh.portal.entity;

import java.util.Date;
import javax.persistence.*;

@Table(name = "eip_info")
public class EipInfo {
    /**
     * 主键
     */
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Integer id;

    /**
     * 公司编码
     */
    @Column(name = "shop_no")
    private String shopNo;

    /**
     * 公司id
     */
    @Column(name = "member_id")
    private Integer memberId;

    /**
     * 注册时间
     */
    @Column(name = "create_date")
    private Date createDate;

    /**
     * 公司qq
     */
    private String qq;

    /**
     * 公司网址
     */
    private String web;

    /**
     * 店铺图片pic{1:,2};键值对的方式
     */
    @Column(name = "shop_img")
    private String shopImg;

    /**
     * 公司邮箱
     */
    private String email;

    /**
     * 固定电话
     */
    private String tel;

    /**
     * 传真
     */
    private String fax;

    /**
     * 简介
     */
    private String remark;

    /**
     * 获取主键
     *
     * @return id - 主键
     */
    public Integer getId() {
        return id;
    }

    /**
     * 设置主键
     *
     * @param id 主键
     */
    public void setId(Integer id) {
        this.id = id;
    }

    /**
     * 获取公司编码
     *
     * @return shop_no - 公司编码
     */
    public String getShopNo() {
        return shopNo;
    }

    /**
     * 设置公司编码
     *
     * @param shopNo 公司编码
     */
    public void setShopNo(String shopNo) {
        this.shopNo = shopNo;
    }

    /**
     * 获取公司id
     *
     * @return member_id - 公司id
     */
    public Integer getMemberId() {
        return memberId;
    }

    /**
     * 设置公司id
     *
     * @param memberId 公司id
     */
    public void setMemberId(Integer memberId) {
        this.memberId = memberId;
    }

    /**
     * 获取注册时间
     *
     * @return create_date - 注册时间
     */
    public Date getCreateDate() {
        return createDate;
    }

    /**
     * 设置注册时间
     *
     * @param createDate 注册时间
     */
    public void setCreateDate(Date createDate) {
        this.createDate = createDate;
    }

    /**
     * 获取公司qq
     *
     * @return qq - 公司qq
     */
    public String getQq() {
        return qq;
    }

    /**
     * 设置公司qq
     *
     * @param qq 公司qq
     */
    public void setQq(String qq) {
        this.qq = qq;
    }

    /**
     * 获取公司网址
     *
     * @return web - 公司网址
     */
    public String getWeb() {
        return web;
    }

    /**
     * 设置公司网址
     *
     * @param web 公司网址
     */
    public void setWeb(String web) {
        this.web = web;
    }

    /**
     * 获取店铺图片pic{1:,2};键值对的方式
     *
     * @return shop_img - 店铺图片pic{1:,2};键值对的方式
     */
    public String getShopImg() {
        return shopImg;
    }

    /**
     * 设置店铺图片pic{1:,2};键值对的方式
     *
     * @param shopImg 店铺图片pic{1:,2};键值对的方式
     */
    public void setShopImg(String shopImg) {
        this.shopImg = shopImg;
    }

    /**
     * 获取公司邮箱
     *
     * @return email - 公司邮箱
     */
    public String getEmail() {
        return email;
    }

    /**
     * 设置公司邮箱
     *
     * @param email 公司邮箱
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * 获取固定电话
     *
     * @return tel - 固定电话
     */
    public String getTel() {
        return tel;
    }

    /**
     * 设置固定电话
     *
     * @param tel 固定电话
     */
    public void setTel(String tel) {
        this.tel = tel;
    }

    /**
     * 获取传真
     *
     * @return fax - 传真
     */
    public String getFax() {
        return fax;
    }

    /**
     * 设置传真
     *
     * @param fax 传真
     */
    public void setFax(String fax) {
        this.fax = fax;
    }

    /**
     * 获取简介
     *
     * @return remark - 简介
     */
    public String getRemark() {
        return remark;
    }

    /**
     * 设置简介
     *
     * @param remark 简介
     */
    public void setRemark(String remark) {
        this.remark = remark;
    }
}